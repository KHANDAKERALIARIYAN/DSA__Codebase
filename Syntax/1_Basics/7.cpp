// Calculating Area of a Circle
#include<iostream>
using namespace std;
int main(){
    float radius = 10;
    float PI = 3.1416;
    float area = PI*radius*radius;
    cout<<"The radius of circle is :"<<radius<<endl;
    cout<<"The area of the circle with radius "<<radius<<" is: "<<area<<endl;
    return 0;
}