#include<iostream>
using namespace std;
int main(){
    cout<<"Arithmetic Operations in C++"<<endl;
    int x=10;
    int y=2;
    cout<<"X+Y = "<<x+y<<endl; // Addition
    cout<<"X-Y = "<<x-y<<endl; // Subtraction
    cout<<"X*Y = "<<x*y<<endl; // Multiplication
    cout<<"X/Y = "<<x/y<<endl; // Division
    cout<<"X%Y = "<<x%y<<endl; // Modulus
    cout<<"X++ = "<<x++<<endl; // Post-increment
    cout<<"++X = "<<++x<<endl; // Pre-increment
    cout<<"Y-- = "<<y--<<endl; // Post-decrement
    cout<<"--Y = "<<--y<<endl; // Pre-decrement
    return 0;
}